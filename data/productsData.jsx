function getProductDataById(id) {
    const product = productsData.find(product => product.id == id)

    return product;
}

function searchFilter(value) {
    const searchPattern = new RegExp(value, 'gi');
    if(value === ""){
        return 
    }
    const searchList = productsData.filter(product => searchPattern.test(product.title));

    if (searchList.length < 1) {
        return 
    }

    return searchList;
}

